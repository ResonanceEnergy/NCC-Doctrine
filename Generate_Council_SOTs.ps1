# Function to create Council SOT
function New-CouncilSOT {
    param(
        [string]$CouncilName,
        [string]$Domain
    )

    $councilPath = Join-Path $CouncilsSOTPath $CouncilName
    $profilePath = Join-Path $councilPath "council_sot.md"

    # Create council directory if it doesn't exist
    if (!(Test-Path $councilPath)) {
        New-Item -ItemType Directory -Path $councilPath -Force | Out-Null
    }

    # Generate SOT content
    $sotContent = $councilSOTTemplate
    $sotContent = $sotContent -replace '\{COUNCIL_NAME\}', $CouncilName
    $sotContent = $sotContent -replace '\{DOMAIN\}', $Domain

    # Write SOT file
    $sotContent | Set-Content -Path $profilePath -Encoding UTF8

    Write-Host "Created SOT for council: $CouncilName" -ForegroundColor Green
}

