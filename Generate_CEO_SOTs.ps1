# Function to create CEO SOT
function New-CEOSOT {
    param(
        [string]$ExecutiveTitle,
        [string]$Domain
    )

    $executivePath = Join-Path $CEOsSOTPath $ExecutiveTitle
    $profilePath = Join-Path $executivePath "executive_sot.md"

    # Create executive directory if it doesn't exist
    if (!(Test-Path $executivePath)) {
        New-Item -ItemType Directory -Path $executivePath -Force | Out-Null
    }

    # Generate SOT content
    $sotContent = $ceoSOTTemplate
    $sotContent = $sotContent -replace '\{EXECUTIVE_TITLE\}', $ExecutiveTitle
    $sotContent = $sotContent -replace '\{DOMAIN\}', $Domain

    # Write SOT file
    $sotContent | Set-Content -Path $profilePath -Encoding UTF8

    Write-Host "Created SOT for executive: $ExecutiveTitle" -ForegroundColor Green
}

